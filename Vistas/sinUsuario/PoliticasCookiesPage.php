            <h1>AVISO SOBRE EL USO DE COOKIES</h1>
            <p>Casa Greciano desea informarte sobre el uso de cookies en nuestras páginas web.</p>
            <h3>¿Qué son las cookies?</h3>
            <p>Una cookie es un archivo o dispositivo que se descarga en el ordenador/smartphone/tablet del usuario al
                acceder a determinadas páginas web para almacenar y recuperar información del equipo terminal. Su
                función es permitir almacenar y recuperar información sobre los hábitos de navegación del usuario con el
                fin de mejorar el servicio ofrecido.</p>
            <h3>Tipo de cookies</h3>
            <p>Existen diferentes tipos de cookies:</p>
            <p>1. Según la entidad que gestione el dominio desde donde se envían las cookies:</p>
            <p>- Cookies propias: son aquéllas que se envían a tu equipo desde nuestros propios equipos o dominios.</p>
            <p>- Cookies de terceros: son aquéllas que se envían a tu equipo desde un equipo o dominio de otra entidad
                colaboradora.</p>
            <p>2. Según el plazo de tiempo que permanecen almacenadas en el navegador del cliente:</p>
            <p>- Cookies de sesión: aquellas cookies activadas mientras el usuario accede a la página web o para la
                prestación del servicio solicitado.</p>
            <p>- Cookies persistentes: aquellas cookies almacenadas por un tiempo determinado en el equipo terminal del
                usuario, para las que el responsable tiene acceso cuando el usuario se conecta a su página web.</p>
            <p>3. Según la finalidad para la que se traten los datos obtenidos:</p>
            <p>- Cookies técnicas: aquellas cookies necesarias para el uso del sitio web y para la prestación de
                servicios.</p>
            <p>- Cookies de personalización: aquellas que permiten al usuario acceder al servicio con unas algunas
                características predefinidas en función de una serie de criterios en el terminal del usuario (idioma,
                tipo de navegador…).</p>
            <p>- Cookies de análisis: aquellas que permiten el seguimiento y análisis estadístico del comportamiento de
                los usuarios de los sitios web.</p>
            <p>- Cookies publicitarias: aquéllas que permiten la gestión de los espacios publicitarios en la página web,
                aplicación o plataforma desde la que se presta el servicio solicitado.</p>
            <p>- Cookies de publicidad comportamental: aquellas que almacenan información del comportamiento del
                usuario, lo que permite desarrollar un perfil específico para mostrar publicidad.</p>
            <h3>Cookies utilizadas en nuestro sitio web</h3>
            <p>A continuación se identifican las cookies que están siendo utilizadas en nuestro sitio web:</p>
            <p>· Cookies de personalización y técnicas estrictamente necesarias para la prestación de determinados
                servicios solicitados expresamente por el usuario: si se desactivan estas cookies, no podrá recibir
                correctamente nuestros contenidos y servicios.</p>
            <p>· Cookies de análisis para el seguimiento y análisis estadístico del comportamiento de los usuarios.</p>
            <p>La presente Política de Cookies podrá ser objeto de modificaciones, por lo que se recomienda al usuario
                que la consulte cada vez que acceda al sitio web de Casa Greciano.</p>
            <h3>Cómo modificar la configuración de las cookies</h3>
            <p>El usuario puede prestar su consentimiento al uso de cookies o revocarlo en cualquier momento utilizando
                su navegador, por el que puede permitir, restringir, bloquear o borrar las cookies utilizadas desde
                nuestro Sitio Web.</p>
            <p>La forma de hacerlo será diferente en función del tipo de navegador utilizado durante la navegación. En
                los siguientes enlaces tiene a su disposición toda la información para configurar o deshabilitar las
                cookies en cada navegador:</p>
            <p>Internet Explorer: windows.microsoft.com/es-xl/internet-explorer/delete-manage-cookies#ie="ie-10"
                Firefox: support.mozilla.org/es/kb/Borrar%20cookies
                Chrome: support.google.com/chrome/answer/95647?hl="es"
                Safari: www.apple.com/es/privacy/use-of-cookies/
            </p>
            <p>El usuario puede prestar su consentimiento al uso de cookies o revocarlo en cualquier momento a través
                del enlace Configuración de Cookies.</p>
